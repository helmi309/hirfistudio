        <p class="titile-home-content">Tentang Hirfi Studio</p>
        <p class="margin-content" id="par-history">
          Berawal dari aktifitas dan kegiatan sebagai tenaga ahli <em>freelance</em> dalam bidang perencanaan transportasi, pengukuran dan tata ruang di beberapa perusahaan konsultan besar di Jawa Timur,maka berkumpullah kami para <em>engineer</em> muda ini untuk memulai pengabdian dan karir profesional kami sebuah melalui perusahaan yang mandiri, <b>hirfi studio</b>.
        </p>
        <p class="margin-content indent-top"><b>Hirfi studio</b> didirikan di Malang pada tanggal 7 Juli 2010. Sekalipun masih relatif muda, namun para engineer HIRFI adalah personil yang telah mempunyai pengalaman yang sangat banyak dalam bidang-bidang pengembangan sistem transportasi, pengembangan jaringan jalan, pembuatan kajian kelayakan prasarana transportasi, penyusunan dokumen tata ruang serta pembuatan detil rencana pengembangan pelabuhan, bandar udara dan infrastruktur transportasi lainnya. 
        </p>
        <p class="margin-content indent-top">Hirfi sendiri berasal dari bahasa <em><u>Arab</u></em>, yang artinya <em><u>professional</u></em>. Itulah yang ingin dicapai dari pendirian perusahaan ini, yaitu menjadi perusahaan yang didukung oleh tenaga-tenaga profesional yang melakukan pekerjaannya secara profesional. Selain itu, pendirian <b>hirfi studio</b> dimaksudkan untuk lebih memaksimalkan kemampuan para <em>engineernya</em> dalam melakukan telaah, kajian dan perencanaan bidang transportasi, tata ruang dan geodesi melalui inovasi dan penggunaan teknik dan analisis terbaru dalam bidang-bidang di atas. 
        </p>
        <p class="margin-content indent-top">Dengan nama, pengalaman serta kualifikasi <em>engineer</em> pendukungnya, <b>hirfi studio</b> siap memberikan jasa konsultansinya kepada pihak pemberi pekerjaan dengan penuh tanggung jawab dan profesional.
        </p>
